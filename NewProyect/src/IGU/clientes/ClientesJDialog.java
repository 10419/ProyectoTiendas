    private void jButtonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrabarActionPerformed
        Cliente dp= new Cliente();
        dp.setNombre(v_nombre.getText());
        dp.setAp_paterno(v_paterno.getText());
        dp.setAp_materno(v_materno.getText());
        dp.setCodigo(v_dni.getText());
        dp.setUsuario(v_usuario.getText());
        dp.setPassword(v_contrasenia.getText());

        if(v_femenino.isSelected()==true){
            dp.setSexo("F");
        }
        if(v_masculino.isSelected()==true){
            dp.setSexo("M");
        }

        if(!v_nombre.getText().equals("")|| !v_paterno.getText().equals("") || !v_materno.getText().equals("") || !v_dni.getText().equals("")  ){
            metodos.registrarPersona(dp);
            v_nombre.setText("");
            v_paterno.setText("");
            v_materno.setText("");
            v_usuario.setText("");
            v_contrasenia.setText("");
            v_dni.setText("");

            JOptionPane.showMessageDialog(
                null,
                "Se registro con Exito",
                "Registro",
                JOptionPane.INFORMATION_MESSAGE);

            jTablePersonas.removeAll();
            ReporteData();
        }else{
            JOptionPane.showMessageDialog(
                null,
                "No se registro",
                "Error/Advertencia",
                JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButtonGrabarActionPerformed

